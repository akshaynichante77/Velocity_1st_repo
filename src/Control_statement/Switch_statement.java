package Control_statement;

public class Switch_statement {

	public static void main(String[] args) {
		int year = 5 ;
		
		switch (year) {
		case 1 : 
			System.out.println("wlc to 1st yr engg");
			break;
			
		case 2 : 
			System.out.println("wlc to 2st yr engg");
			break;
			
		case 3 : 
			System.out.println("wlc to 3st yr engg");
			break;
			
		case 4 : 
			System.out.println("wlc to 4st yr engg");
			break;
			
			default : 
				System.out.println("Pls entr value between 1-4");
		}
	}

}

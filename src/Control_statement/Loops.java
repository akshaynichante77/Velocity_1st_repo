package Control_statement;

public class Loops {

	public static void main(String[] args) {
// for (initilize and condition)
		
		for (int i = 1; i<=5;i++) // For loop
		{
			System.out.println(i);//1,2,3,4,5
		}
		
//==========================================================
		
		int a = 1;
		while (a<=10) // while loop 
		{
			System.out.println(a);// 1,2...9,10
			a++;// if we do not declare then continuosly run
		}
		
	}

}
